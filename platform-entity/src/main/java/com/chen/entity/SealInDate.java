package com.chen.entity;

public class SealInDate {
    private int id;
    private String seal_indate_code;
    private int default_indate;
    private String seal_indate_name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getSeal_indate_code() {
        return seal_indate_code;
    }

    public void setSeal_indate_code(String seal_indate_code) {
        this.seal_indate_code = seal_indate_code;
    }

    public int getDefault_indate() {
        return default_indate;
    }

    public void setDefault_indate(int default_indate) {
        this.default_indate = default_indate;
    }

    public String getSeal_indate_name() {
        return seal_indate_name;
    }

    public void setSeal_indate_name(String seal_indate_name) {
        this.seal_indate_name = seal_indate_name;
    }
}
